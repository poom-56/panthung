# งบประมาณเริ่มต้น
budget = 1000.0

# สินค้าและราคาทั้งหมด (dictionary)
products = {
    "หมูสับกิโล": 150.00,
    "เนื้ออกไก่": 105.00,
    "ไก่บ้าน(ตัว)": 120.00,
    "มะนาวต้มยำ": 6.50,
    "ข้าวสาร": 150.00,
    "น้ำตาล": 20.00,
    "ปลากระป๋องสามแม่ครัว": 10.00,
    "ซอสหอยนางรม": 18.00,
    "ผงชูรส": 10.25,
    "ไข่แยคละเบอร์": 120.25,
    "เทียนไข": 21.50,
    "Pepsi": 29.50,
    "กาแฟ": 15.75,
    "ซุปหมูปิ้งขนมปัง": 19.00,
    "ชาไทย": 11.50,
    "น้ำเปล่า": 15.15,
    "น้ำแข็ง": 10.00
}

# รายการที่นักเรียนเลือกซื้อ (อย่างน้อย 15 รายการ)
my_cart = [
    "หมูสับกิโล",
    "เนื้ออกไก่",
    "ไก่บ้าน(ตัว)",
    "มะนาวต้มยำ",
    "ข้าวสาร",
    "น้ำตาล",
    "ปลากระป๋องสามแม่ครัว",
    "ซอสหอยนางรม",
    "ผงชูรส",
    "ไข่แยคละเบอร์",
    "เทียนไข",
    "Pepsi",
    "กาแฟ",
    "ซุปหมูปิ้งขนมปัง",
    "ชาไทย"
]

# คำนวณราคารวม
total_cost = sum(products[item] for item in my_cart)

# คำนวณเงินที่เหลือ
remaining = budget - total_cost

# แสดงผล
print("คุณซื้อของทั้งหมด {} รายการ".format(len(my_cart)))
print("ราคารวม: {:.2f} บาท".format(total_cost))
print("เงินที่เหลือ: {:.2f} บาท".format(remaining))

