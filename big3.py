# แบบฝึกหัด: การใช้ Set ใน Python
a = {10, 20, 30, 40, 50, 60}
b = {30, 40, 50, 60, 70, 80}

print("ผลลัพธ์ที่อยู่ทั้งใน a และ b:", a & b)
print("ผลลัพธ์ที่อยู่ใน a แต่ไม่อยู่ใน b:", a - b)
print("ผลลัพธ์ที่อยู่ใน b แต่ไม่อยู่ใน a:", b - a)
print("ผลรวมของ a และ b:", a | b)
print("ผลต่างแบบสมมาตร (symmetric difference):", a ^ b)
