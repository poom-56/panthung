# รับค่าคะแนนสองค่า แล้วหาค่าเฉลี่ย
score1 = float(input("ป้อนค่าคะแนนที่ 1: "))  # เช่น 0.25
score2 = float(input("ป้อนค่าคะแนนที่ 2: "))  # เช่น 5

average = (score1 + score2) / 2

# แสดงผลลัพธ์ทศนิยม 6 ตำแหน่ง
print("ค่าเฉลี่ยคือ: {:.6f}".format(average))
