# แปลงองศาเซลเซียสเป็นฟาเรนไฮต์ และเคลวิน
celsius = float(input("ป้อนอุณหภูมิในหน่วยองศาเซลเซียส: "))

fahrenheit = (9/5) * celsius + 32
kelvin = celsius + 273.15

print("อุณหภูมิในหน่วยฟาเรนไฮต์ (F):", fahrenheit)
print("อุณหภูมิในหน่วยเคลวิน (K):", kelvin)

